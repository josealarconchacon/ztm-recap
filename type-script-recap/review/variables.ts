// string, numbers, booleans, null, undefined
// 1. String: A sequence of characters
const myString: string = "Hello World";
console.log(myString);

// 2. Numbers: Represent a quantity or amount
const myNumber: number = 42;
console.log(myNumber);

// 3. Boolean: Represents true or false values
const isDone: boolean = true;
console.log(isDone);

// 4. Null: Used to indicate that something is empty/does not exist
let x = null;
console.log(x);

// 5. Undefined: Used to indicate that a variable has been declared but not assigned a value yet
let y;
console.log(y);
